    public void clear()
    {
        textName.setText("");
        textEmail.setText("");
        textMobile.setText("");
        textAddress.setText("");
        textSecurity.setText("");
        textAnswer.setText("");
        textPassword.setText("");
        btnSave.setEnabled(false);
    }
    
    public void validateFields()
    {
        String name=textName.getText();
        String email=textEmail.getText();
        String mobileNumber=textMobile.getText();
        String address=textAddress.getText();
        String password=textPassword.getText();
        String securityQuestion=textSecurity.getText();
        String answer=textAnswer.getText();
        if(!name.equals("") && email.matches(emailPattern) && mobileNumber.matches(mobileNumberPattern) && mobileNumber.length()==10 && !address.equals("") && !password.equals("") && !securityQuestion.equals("") && !answer.equals(""))
            btnSave.setEnabled(true);
        else 
            btnSave.setEnabled(false);
    }
