        user.setEmail(textEmail.getText());
        user.setMobileNumber(textMobile.getText());
        user.setAddress(textAddress.getText());
        user.setPassword(textPassword.getText());
        user.setSecurityQuestion(textSecurity.getText());
        user.setAnswer(textAnswer.getText());
        UserDao.save(user);
        setVisible(false);
        new Login().setVisible(true);
